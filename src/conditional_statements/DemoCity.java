package conditional_statements;

public class DemoCity {

    public static void main(String[] args) {

        String city="Pune";

        switch (city){

            case "AGRA":
                System.out.println("You are in Agra...");
                break;
            case "Delhi":
                System.out.println("You are in Delhi...");
                break;
            case "Pune":
                System.out.println("You are in Pune...");
                break;
            default:
                System.out.println("No Match Found...");

        }

    }




}
