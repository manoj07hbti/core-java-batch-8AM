package variables;

public class DemoVariables {

    public static void main(String[] args) {

        // SYNTAX : DATATYPE var_name= value ;

        String student_name= "Rahul" ;

        System.out.println("Printing variables " +student_name);

        String cityName="AGRA";

        System.out.println("City NAME IS "+cityName);

        int age=23;

        System.out.println("Printing age "+age);

        int distance=200;

        System.out.println("Printing distance "+distance);
        double salary=23456.7;
        System.out.println("Printing Salary "+salary);

        double price=234.9;

        System.out.println("Price is "+price);



    }
}
